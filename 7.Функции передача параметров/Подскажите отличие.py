arr = [2, 1, 5, 3, 4]
# Дан массив неотсортированных чисел

print(sorted(arr))
# Возвращает новые массив, с уже отсортированными числами из оригинального массива

nem_arr = sorted(arr)
# Функция sorted создала новый массив, и мы записали его в new_arr
print(nem_arr)
# Выводится отсортированный массив

# .sort() изменяет уже существующий массив, поэтому мы не можем сразу же его вывести
arr.sort()
print(arr)

# Итог: sorted создает новый массив, в то время как sort изменяет существующий
